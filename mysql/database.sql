  `ID` INT NOT NULL AUTO_INCREMENT,
  `GCD` INT NOT NULL,
  `COMPUTED_DATE` DATETIME NOT NULL,
  PRIMARY KEY (`ID`),
  UNIQUE INDEX `ID_GCD_RESULT` (`ID` ASC));  